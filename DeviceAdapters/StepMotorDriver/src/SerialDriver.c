#include "SerialDriver.h"
/************************************************************
  
************************************************************/
void  InitSerial()
{

    SCON  = 0x50;		         
    TMOD |= 0x20;               
    TH1   = 0xF3;               
    TR1   = 1;                                          
    EA    = 1;                  
    ES    = 1;                  
}                            
/************************************************************
  
************************************************************/
void SendByte(unsigned char dat)
{
 SBUF = dat;
 while(!TI);
      TI = 0;
}
/************************************************************
  
************************************************************/
void SendStr(unsigned char *s)
{
s--;
 while(*s!='\0') 
  {
  SendByte(*s);
  s++;
  }
}
